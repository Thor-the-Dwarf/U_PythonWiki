# In Python müssen Variablen nicht vorab deklariert werden.
# Der Datentyp wird automatisch erkannt.

# Ein Wahrheitswert (Boolean) zuweisen:
is_happy = True
print("Wert von is_happy:", is_happy)
print("Typ von is_happy:", type(is_happy))

# Zum Beispiel, eine Ganzzahl (Integer) zuweisen:
x = 10
print("Wert von x:", x)
print("Typ von x:", type(x))

# Eine Fließkommazahl (float) zuweisen:
pi = 3.14159
print("Wert von pi:", pi)
print("Typ von pi:", type(pi))

# Einen String zuweisen:
name = "John"
print("Wert von name:", name)
print("Typ von name:", type(name))

# Eine Liste zuweisen:
fruits = ["Apple", "Banana", "Cherry", 12] # Variable declarieren
print("Wert von fruits:", fruits)
print("Typ von fruits:", type(fruits)) # Typ von fruits: <class 'list'>
# einzelne elemente einer Liste ausgeben
print(fruits[0])    # Apple
print(fruits[1])    # Banana
print(fruits[2])    # Cherry
print(fruits[3])    # 12
print(type(fruits[3]))    # <class 'Integer'>
print(type(fruits[2]))  # <class 'str'>

# Ein Dictionary zuweisen:
person = {"name": "John", "age": 30}
print("Wert von person:", person)
print("Typ von person:", type(person))

